export const getFeaturedBooks = () => books.filter((b) => b.isNew).slice(0, 6);

export const getBooksByCategory = (categoryId) => {
  if (categoryId === "all") return books;
  return books.filter((b) => b.category === categoryId);
};

export const getBookById = (id) => books.find((b) => b.id === Number(id));

export const getRelatedBooks = (book, limit = 4) =>
  books.filter((b) => b.category === book.category && b.id !== book.id).slice(0, limit);
